package code._4_student_effort.Challenge2;

public class Premiu {
    private String nume;
    private int year;

    public Premiu(String nume, int year) {
        this.nume = nume;
        this.year = year;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
